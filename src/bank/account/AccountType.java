// Ein Enum enthählt keine Implementierungen

// Ein Enum definiert eine Konstante. Es ist eine Klasse mit einer
// vordefinierten Menge von Objekten (hier PERSONAL, SAVINGS).

// Ein Enum macht dort Sinn wo wir eine fixe Anzahl an Werten haben. Wenn die
// Werte immer gleichbleiben und immer wieder verwendet werden. Z.B. Wochentage
// Monate oder wie hier Bankkonten